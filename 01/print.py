# print Hello,JRT!
print("Hello,JRT!")
# print Hello,JRT!,Hello,World!--->print函数接受可变数量的参数
print("Hello,JRT!", "Hello,World!")
# triple quoted strings,三引号括起来的带有格式的字符串
print("""Hello,JRT!
         Hello,World!""")
print('''Hello,JRT!
         Hello,World!''')
# 不仅仅可以接受字符串来输出（最终会转化为字符串类型）
print("1 + 1 =", 1+1)
print("2 * 5 =", 2*5)
print("9 / 3 =", 9/3)
print("1 - 1 =", 1-1)

